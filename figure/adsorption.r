library(ggplot2)
P <- c(0,10,30,50,80,120,150,220,280,330,360,400,430,470,500)
L1 <- c(0,1,2,3,4,5,6,12,22,32,37,45,47,48,49)
q <- c(220,225,260,280,310,400,500)
qq <- c(12,16,32,40,45,48,49)
#pdf("figure/5.pdf")
p1 <- ggplot()+geom_line(aes(x=P,y=L1),size=2)+geom_line(aes(x=q,y=qq),size=2)+geom_point(aes(x=P,y=L1),size=4,color="blue")+geom_point(aes(x=q,y=qq),size=4,color="blue") + ylab("Average loading\n(molecular/cell)") + xlab("Pressure(kPa)") + ggtitle("V")
p1 + theme(plot.title = element_text(hjust = 0.5), text = element_text(size=16), legend.position = "bottom")
